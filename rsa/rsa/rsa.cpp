// Попытка разложить число n на простые множители p, q (При условии что n - маленькое число)
// Перебором по очереди генерируем p, q и проверяем чтобы подошло число, p * q = n; p,q > 1
bool findFactors(int n, int &p, int &q) {
	for (int i = 2; i * i <= n; ++i) {
		if (n % i == 0) {
			p = i;
			q = n / i;

			return true;
		}
	}

	return false;
}

