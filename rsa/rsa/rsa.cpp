/* Быстрое возведение в степень по модулю
  Цикл реализует быстрое возведение в степень.На каждом шаге проверяется младший бит показателя степени.Если он равен 1, текущая степень основания включается в результат.Затем основание возводится в квадрат, а показатель делится на 2.*/
int modPow(int base, int exp, int mod) {
	int result = 1;
	base %= mod; // сразу пытаемся уменьшить число

	while (exp > 0) { // постепенно будем уменьшать степень ехр
		// Алгоритм основан на разложении степени по двоичной записи.
		if (exp % 2 == 1)
			result = (result * base) % mod; // выполняется только если нечетная степень
								// Мы умножаем текущий результат на base и сразу берём по модулю.
								// Это позволяет не держать огромные числа.

		// Мы не идём по степени по одному шагу, а удваиваем степень каждый раз:
		base = (base * base) % mod; // 31 - 31^2 - 31^4 - 31^8
		exp /= 2;					// сдвиг по битам вправо 1101 - 110 - 11 - 1 - 0
	}

	return result;
}


